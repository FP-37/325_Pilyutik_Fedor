"""
Входные данные
Дана строка.

Выходные данные
Сначала выведите третий символ этой строки.

Во второй строке выведите предпоследний символ этой строки.

В третьей строке выведите первые пять символов этой строки.

В четвертой строке выведите всю строку, кроме последних двух символов.

В пятой строке выведите все символы с четными индексами (считая, что индексация начинается с 0, поэтому символы
выводятся начиная с первого).

В шестой строке выведите все символы с нечетными индексами, то есть начиная со второго символа строки.

В седьмой строке выведите все символы в обратном порядке.

В восьмой строке выведите все символы строки через один в обратном порядке, начиная с последнего.

В девятой строке выведите длину данной строки.
"""

# Ввод строки
input_string = input()

# Вывод третьего символа
third_char = input_string[2]
print(third_char)

# Вывод предпоследнего символа
second_to_last = input_string[-2]
print(second_to_last)

# Вывод первых пяти символов
first_five = input_string[:5]
print(first_five)

# Вывод всей строки без последних двух символов
without_last_two = input_string[:-2]
print(without_last_two)

# Вывод символов с четными индексами
even_indices = input_string[0::2]
print(even_indices)

# Вывод символов с нечетными индексами
odd_indices = input_string[1::2]
print(odd_indices)

# Вывод строки в обратном порядке
reverse_string = input_string[::-1]
print(reverse_string)

# Вывод символов строки через один в обратном порядке
reverse_every_other = input_string[-1::-2]
print(reverse_every_other)

# Вывод длины строки
string_length = len(input_string)
print(string_length)