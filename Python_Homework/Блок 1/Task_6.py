"""
Дана строка. Найдите в этой строке второе вхождение буквы f, и выведите индекс этого вхождения.
Если буква f в данной строке встречается только один раз,
выведите число -1, а если не встречается ни разу, выведите число -2.

При решении этой задачи нельзя использовать метод count.

Входные данные
Вводится строка.

Выходные данные
Выведите ответ на задачу.
"""
# Ввод строки
input_string = input()

# Переменная для хранения индекса первого вхождения 'f'
first_index = input_string.find('f')

# Переменная для хранения индекса второго вхождения 'f'
second_index = -1

# Поиск индексов первого и второго вхождения буквы
if first_index != -1:
    # Находим второе вхождение 'f' после первого
    input_string = input_string[first_index + 1:]
    second_index = input_string.find('f')

    # Если второе вхождение найдено, обновляем индекс
    if second_index != -1:
        second_index = first_index + second_index + 1

# Обработка случаев, когда буква встречается один или ни одного раза
if first_index == -1:
    print("-2")
elif second_index == -1:
    print("-1")
else:
    print(second_index)