"""
Напишите программу, которая будет выполнять последовательность запросов вида ADD num, PRESENT num и COUNT
(без параметра). Программу обязательно следует писать с использованием шаблонного типа set.

Выполнение каждого запроса вида ADD num должно добавлять элемент num во множество (если такой элемент уже есть,
добавление ещё одной копии не изменяет множество), на экран при этом ничего не выводится.

При выполнении каждого запроса вида PRESENT num должно выдаваться сообщение «YES» или «NO»
(большими буквами, в отдельной строке), соответственно тому, есть ли такой элемент во множестве; значение множества при
этом не изменяется.

При выполнении каждого запроса вида COUNT должна выдаваться на экран в отдельной строке текущее количество различных
элементов в множестве; значение множества при этом не изменяется.

Входные данные
В первой строке стандартного входного потока задано количество запросов N (1<N<100000),
далее следуют N строк, каждая из которых содержит по одному запросу согласно описанного формата.

Значения чисел не превышают по модулю 100000000.

Выходные данные
Выводите на стандартный выход (экран) в отдельных строках результаты запросов PRESENT и COUNT;
на запросы ADD ничего выводить не надо.
"""
def main():
    # Инициализация множества
    my_set = set()

    # Чтение количества запросов
    N = int(input().strip())

    # Обработка запросов
    for _ in range(N):
        query = input().strip().split()
        if query[0] == 'ADD':
            my_set.add(int(query[1]))
        elif query[0] == 'PRESENT':
            num = int(query[1])
            if num in my_set:
                print("YES")
            else:
                print("NO")
        elif query[0] == 'COUNT':
            print(len(my_set))

if __name__ == "__main__":
    main()