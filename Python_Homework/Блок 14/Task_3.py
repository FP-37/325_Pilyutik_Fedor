"""
В лицее на уроках информатики ответы учеников оцениваются целым числом баллов от 2 до 5.
Итоговая оценка по информатике выставляется как среднее арифметическое оценок на всех уроках,
округленное до ближайшего целого числа. Если среднее значение находится ровно посередине между двумя целыми числами,
то оценка округляется вверх.

Все ученики лицея стремятся получить итоговую оценку по информатике не ниже 4 баллов.
К сожалению, один из учеников получил на уроках a двоек, b троек и c четверок.
Теперь он планирует получить несколько пятерок, причем хочет, чтобы итоговая оценка была не меньше 4 баллов.
Ему надо понять, какое минимальное количество пятерок ему необходимо получить, чтобы добиться своей цели.

Требуется написать программу, которая по заданным целым неотрицательные числам a, b и c определяет
минимальное количество пятерок, которое необходимо получить ученику, чтобы его итоговая оценка по информатике
была не меньше 4 баллов.

Входные данные
Входные данные содержат три строки. Первая строка содержит целое неотрицательное число a,
вторая строка содержит целое неотрицательное число b, третья строка содержит
целое неотрицательное число c (0≤a,b,c≤10^15,a+b+c≥1).

Выходные данные

Выходные данные должны содержать одно число: минимальное число пятерок, которое необходимо получить ученику,
чтобы итоговая оценка была не меньше 4 баллов.

Примечание

Следует обратить внимание, что входные данные в этой и других задачах не помещаются в стандартный 32 -битный тип данных.
Необходимо использовать 64 -битный тип данных (long long в С++, int64 в Паскале, long в Java).

Система оценки

Баллы за каждую подзадачу начисляются только в случае, если все тесты для этой подзадачи
и необходимых подзадач успешно пройдены.
Подзадача 1: 13 баллов, 1≤a≤100, b = 0, c = 0 (ученик получал только двойки).
Подзадача 2: 14 баллов, a = 0, 1≤b≤100, c = 0 (ученик получал только тройки).
Подзадача 3: 15 баллов, 0≤a,b,c≤100, необходимые подзадачи: 1,2.
Подзадача 4: 28 баллов, 0≤a,b,c≤10^6, необходимые подзадачи: 1,2,3.
Подзадача 5: 30 баллов, 0≤a,b,c≤10^15, необходимые подзадачи: 1,2,3,4.
"""

def min_fives(a, b, c):
    total_grades = a+b+c
    target_average = 4

    def is_valid(num_fives):
        total_score = a*2+b*3+c*4+num_fives*5
        average_score = total_score/(a+b+c+num_fives)
        rounded_average = int(average_score+0.5)
        return rounded_average >= target_average

    left, right = 0, total_grades
    while left <= right:
        mid = (left+right)//2
        if is_valid(mid):
            right = mid-1
        else:
            left = mid+1

    return left

def main():
    a = int(input().strip())
    b = int(input().strip())
    c = int(input().strip())

    print(min_fives(a, b, c))

if __name__ == "__main__":
    main()