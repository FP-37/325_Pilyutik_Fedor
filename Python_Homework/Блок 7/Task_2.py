"""
Во входном файле (вы можете читать данные из файла input.txt) записан текст. Словом считается последовательность
непробельных символов идущих подряд, слова разделены одним или большим числом пробелов или символами конца строки.

Для каждого слова из этого текста подсчитайте, сколько раз оно встречалось в этом тексте ранее.

Входные данные
Вводится текст.

Выходные данные
Выведите ответ на задачу.
"""
def main():
    # Открываем файл для чтения
    with open('input.txt', 'r') as file:
        # Читаем содержимое файла
        text = file.read()

        # Разделяем текст на слова
        words = text.split()

        # Словарь для подсчета вхождений слов
        word_count = {}
        repeats = []

        # Проходимся по всем словам
        for word in words:
            # Если слово уже встречалось, увеличиваем счетчик
            if word in word_count:
                word_count[word] += 1
            # Иначе начинаем счетчик с 0
            else:
                word_count[word] = 0
            repeats.append(word_count[word])

        # Выводим результат
        print(*repeats)

if __name__ == "__main__":
    main()