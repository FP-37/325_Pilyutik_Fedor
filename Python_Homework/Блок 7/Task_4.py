"""
Как известно, в США президент выбирается не прямым голосованием, а путем двухуровневого голосования. Сначала проводятся
выборы в каждом штате, и определяется победитель выборов в данном штате. Затем проводятся государственные выборы:
на этих выборах каждый штат имеет определенное число голосов — число выборщиков от этого штата. На практике,
все выборщики от штата голосуют в соответствии с результами голосования внутри штата, то есть на заключительной стадии
выборов в голосовании участвуют штаты, имеющие различное число голосов.

Вам известно за кого проголосовал каждый штат и сколько голосов было отдано данным штатом. Подведите итоги выборов:
для каждого из участника голосования определите число отданных за него голосов.

Входные данные
Каждая строка входного файла содержит фамилию кандидата, за которого отдают голоса выборщики этого штата, затем через
пробел идет количество выборщиков, отдавших голоса за этого кандидата.

Выходные данные
Выведите фамилии всех кандидатов в лексикографическом порядке, затем, через пробел, количество отданных за них голосов.
"""
def main():
    # Открываем файл для чтения
    with open('input.txt', 'r') as file:
        # Создаем словарь для подсчета голосов за каждого кандидата
        candidate_votes = {}

        # Читаем строки из файла и добавляем голоса к каждому кандидату
        for line in file:
            candidate, votes = line.strip().split()
            candidate_votes[candidate] = candidate_votes.get(candidate, 0) + int(votes)

        # Сортируем кандидатов по фамилии в лексикографическом порядке
        candidates = sorted(candidate_votes.keys())

        # Выводим результаты
        for candidate in candidates:
            print(candidate, candidate_votes[candidate])

if __name__ == "__main__":
    main()