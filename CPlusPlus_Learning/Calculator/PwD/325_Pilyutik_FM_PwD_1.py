"""
Дана последовательность строк, завершающаяся пустой строкой. На их основе создать словарь,
в котором ключами выступают слова, а значениями - списки строк, содержащих данные слова.
Регистр и символы ',:;.?!-' не учитывать. Вывести словарь на экран в следующем формате:
для каждой пары key:value вывести key в отдельной строке, затем элементы value по одному в строке, затем пустую строку.
"""

# Инициализация пустого словаря
dctnr = {}

# Чтение строк до пустой строки
print("Введите строки (и пустую строку для завершения): ")
while True:
    stroka = input().strip()
    if not stroka:
        break

    # Преобразование строки в нижний регистр и удаление ненужных символов
    stroka = stroka.lower()
    stroka = ''.join(ch for ch in stroka if ch not in ',:;.?!-')

    # Разбиение строки на слова и добавление их в словарь
    for word in stroka.split():
        if word not in dctnr:
            dctnr[word] = []
        dctnr[word].append(stroka)

# Вывод словаря
for key, value in dctnr.items():
    print(key)
    for stroka in value:
        print(stroka)
    print()